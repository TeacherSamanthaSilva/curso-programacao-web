        <h3 class="flow-text">O que é o Biblos</h3>
        
      
            <p class="flow-text">Este projeto detalha o desenvolvimento de um jogo para dispositivos móveis batizado de Biblos a Bíblia gamificada, trata-se de uma nova tecnologia criada para ajudar pessoas de todas as idades a desenvolver o hábito da leitura diária da Bíblia. Biblos a bíblia gamificada é um jogo para celulares e tablets criado para dar aqueles que desejam evoluir nos seus estudos bíblicos a motivação necessária para desenvolver o hábito da leitura diária da Bíblia usando como principal ferramenta a gamificação, e assim ajuda-los a ter um melhor entendimento do conteúdo deste livro e crescimento espiritual, tudo isso de uma forma simples e divertida. </p>
          
        
        
        

           <p class="flow-text">O jogo Biblos segue o cânon utilizado pelas bíblias protestantes, que é formado pelos 39 livros do antigo testamento e pelos 27 do novo testamento. Porem em vez do começar pelo livro de Genesis optou-se por começar pelo livro de Filemon, por este ser um dos livros da bíblia com menor quantidade de capítulos. A ideia é começar pelo livro de menor quantidade de capítulos e versículos que é Filemon e terminar no maior livro no quesito quantidade de capítulos e versículos que é Salmos. </p>

      

      
           <p class="flow-text"> A Paráfrase que foi utilizada no desenvolvimento do aplicativo Biblos, a bíblia gamificada foi à Nova Almeida Atualizada desenvolvida e lançada pela Sociedade Bíblica do Brasil em 2017. Pelo fato desta tradução usar uma linguagem de mais fácil entendimento pelo nosso público alvo ser uma das paráfrases mais populares no meio cristão e mais próxima da linguagem coloquial.  </p>

        <p class="flow-text">Para o desenvolvimento do Biblos foi utilizado a engine Unity, A IDE Visual Studio e a linguagem de programação C#.</p>
      

        

       
       

